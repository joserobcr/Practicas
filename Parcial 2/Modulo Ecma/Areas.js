export function areacuadrado(lado){
    return lado*lado;
}

export function areatriangulo(base, altura){
    return ((base*altura)/2);
}

export function suma(num1, num2){
    return num1+num2;
}
export function resta(num1, num2){
    return num1-num2;
}
export function multiplicacion(num1, num2){
    return num1*num2;
}

